void solve() {
    stack<char> s;
    string brackets;cin>>brackets;
    
    int open = 0;
    for(char ch : brackets){
        if (ch == '('){
            s.push(ch);
            open++;
        } else if (open > 0 and ch == ')'){
            s.pop();
            open--;
        }else{
            cout << "NO" << endl;
            return;
        }
    }
    (s.empty() and open == 0) ? cout << "YES" << endl : cout << "NO" << endl;
}

